package app.biarca.operations;
/**this class will give remainder remainder of two input numbers
 *@author anushavedams
 *
 */

public class Remainder {
  /**
   * This will give remainder of two numbers a,b
   * @param a is a number for getting remainder
   * @param b is a number for getting remainder
   * @return remainder of two numbers
   */
	public static int remainder(int a,int b) {
		return a%b;
	}
}
