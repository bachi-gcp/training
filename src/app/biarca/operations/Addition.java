package app.biarca.operations;
/**
 * This class will add two given input numbers
 * @author Bhaskar
 *
 */
public class Addition {
	/**
	 * This will add two numbers a, b
	 * 
	 * @param a is a number for adding
	 * @param b is a number for adding
	 * @return sum of numbers a, b
	 */
	public static int sum(int a, int b) {
		return a+b;
	}
}
