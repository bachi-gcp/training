package app.biarca.operations;
/**
 * This class will multiply two given input numbers
 * @author Bhaskar
 *
 */
public class Multiplication {
	/**
	 * This will multiply two numbers a, b
	 * 
	 * @param a is a number for multiplying
	 * @param b is a number for multiplying
	 * @return product  of numbers a, b
	 */
	public static int multiply(int a, int b) {
		return a*b;
	}
}
